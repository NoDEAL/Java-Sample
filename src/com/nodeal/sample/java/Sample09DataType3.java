package com.nodeal.sample.java;

public class Sample09DataType3 {
	public static void main(String[] args) {
		System.out.println("byte");
		System.out.println(Byte.MAX_VALUE); // byte형의 최대값, 최소값
		System.out.println(Byte.MIN_VALUE);
		System.out.println("\nshort");
		System.out.println(Short.MAX_VALUE); // short형의 최대값, 최소값
		System.out.println(Short.MIN_VALUE);
		System.out.println("\nint");
		System.out.println(Integer.MAX_VALUE); // int형의 최대값, 최소값
		System.out.println(Integer.MIN_VALUE);
		System.out.println("\nlong");
		System.out.println(Long.MAX_VALUE); // long형의 최대값, 최소값
		System.out.println(Long.MIN_VALUE);
		System.out.println("\nfloat");
		System.out.println(Float.MAX_VALUE); // float형의 최대값, 최소값
		System.out.println(Float.MIN_VALUE);
		System.out.println("\ndouble");
		System.out.println(Double.MAX_VALUE); // double형의 최대값, 최소값
		System.out.println(Double.MIN_VALUE);
		System.out.println("\nchar");
		System.out.println(Character.MAX_VALUE); // char형의 최대값, 최소값
		System.out.println(Character.MIN_VALUE);
	}
}
