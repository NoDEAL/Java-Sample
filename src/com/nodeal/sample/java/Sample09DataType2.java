package com.nodeal.sample.java;

public class Sample09DataType2 {
	public static void main(String[] args) {
		/* 데이터 형은 각 자바 클래스로 다시 정의되어 있으며 다음과 같다. */
		int int1 = 1; // 데이터형으로 선언
		Integer int2 = new Integer(1); // 클래스에서 클래스 생성자에 값을 부여하여 선언
		System.out.println(int1);
		System.out.println(int2);
	}
}
